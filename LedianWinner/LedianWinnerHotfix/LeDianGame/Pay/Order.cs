using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Order {

    /// <summary>
    /// 商户id
    /// </summary>
    public string uid;

    /// <summary>
    /// 支付类型
    /// </summary>
    public string type;

    /// <summary>
    /// 商户订单号
    /// </summary>
    public string orderid;

    /// <summary>
    /// 下行异步通知 URL
    /// </summary>
    public string callbackurl;

    /// <summary>
    /// 下行同步跳转 成功   可为空
    /// </summary>
    public string gotrue;

    /// <summary>
    /// 下行同步跳转失败
    /// </summary>
    public string gofalse;

    /// <summary>
    /// 字符编码
    /// </summary>
    public string charset;

    /// <summary>
    /// 备注消息
    /// </summary>
    public string token;

    /// <summary>
    /// MD5签名
    /// </summary>
    public string sign;

    /// <summary>
    /// 商户用户UUID  商户系统内值唯一 减少错单率
    /// </summary>
    public string uuid;
}
