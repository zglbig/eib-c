
using ILRuntime.Reflection;
using System;
using System.Collections.Generic;
using System.Reflection;

public class ObjectModel  {
    private Type objType;
    private object objInstance;
    private Dictionary<string, ILRuntimeParameterInfo[]> parameInfo;
    private Dictionary<string, ILRuntimeMethodInfo> metnodInfo;

    public Type ObjType
    {
        get
        {
            return objType;
        }

        set
        {
            objType = value;
        }
    }

    public object ObjInstance
    {
        get
        {
            return objInstance;
        }

        set
        {
            objInstance = value;
        }
    }

    public Dictionary<string, ILRuntimeMethodInfo> MetnodInfo
    {
        get
        {
            return metnodInfo;
        }

        set
        {
            metnodInfo = value;
        }
    }
    public Dictionary<string, ILRuntimeParameterInfo[]> ParamInfo
    {
        get
        {
            return parameInfo;
        }

        set
        {
            parameInfo = value;
        }
    }
}
